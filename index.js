function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(a) {
  return a += 1;
}

function dec(a) {
  return a -= 1
}

function makeInt() {
  parseInt('a', 10)
  parseInt('878', 10)
}

function preserveDecimal() {
  parseFloat('80.123999')
  parseFloat('a')
}
