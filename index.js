function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(a) {
  return a += 1;
}

function dec(a) {
  return a -= 1
}

function makeInt() {
  parseInt('a', 10) === a
  parseInt('0.2328', 10) === 0
  parseInt('sldkjflksjf')
}

function preserveDecimal() {
  parseFloat('2.222')
  parseFloat('a')
}
